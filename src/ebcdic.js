/**
 * src/ebcdic.js
 * ─────────────────────────────────────────────────────────────────────────────
 * EBCDIC ↔ ASCII / Unicode conversion for TN3270E sessions.
 *
 * Background:
 *   EBCDIC (Extended Binary Coded Decimal Interchange Code) is the character
 *   encoding used by IBM mainframes. Unlike ASCII, which grew out of teletype
 *   standards, EBCDIC was designed around punch card encoding conventions from
 *   the 1960s. The two encodings share no common byte values for printable
 *   characters — 'A' is 0x41 in ASCII but 0xC1 in EBCDIC CP037.
 *
 *   Code pages (CPs) are regional variants. CP037 is the US/Canadian standard.
 *   European installations often use CP273 (Germany), CP285 (UK), CP297
 *   (France), CP500 (International), etc. The code page is agreed upon during
 *   VTAM session setup and stored in the MODE TABLE entry.
 *
 * Supported code pages:
 *   37   — United States / Canada (most common)
 *   273  — Germany / Austria
 *   277  — Denmark / Norway
 *   278  — Finland / Sweden
 *   280  — Italy
 *   284  — Spain / Latin America
 *   285  — United Kingdom
 *   297  — France
 *   500  — International (Belgium, Switzerland)
 *
 * References:
 *   IBM Character Data Representation Architecture (CDRA) — SC09-2190
 *   IBM Code Page Reference — CP037 definition
 * ─────────────────────────────────────────────────────────────────────────────
 */

