// ── Build inverse (ASCII→EBCDIC) tables ──────────────────────────────────
function buildInverse(forwardTable) {
  const inverse = Buffer.alloc(256, 0x3F); // 0x3F = '?' in EBCDIC
  for (let eb = 0; eb < 256; eb++) {
    const asc = forwardTable[eb];
    if (inverse[asc] === 0x3F) inverse[asc] = eb;
  }
  return inverse;
}

const ASCII_TO_CP037 = buildInverse(CP037_TO_ASCII);
const ASCII_TO_CP500 = buildInverse(CP500_TO_ASCII);

// ── Code page registry ────────────────────────────────────────────────────
const CODEPAGES = {
  37:  { toAscii: CP037_TO_ASCII, fromAscii: ASCII_TO_CP037, name: 'CP037 (US/Canada)' },
  500: { toAscii: CP500_TO_ASCII, fromAscii: ASCII_TO_CP500, name: 'CP500 (International)' },
  // Additional code pages can be registered at runtime via registerCodepage()
};

/**
 * Register a custom code page at runtime.
 * @param {number} cpNumber - The IBM code page number (e.g. 273)
 * @param {Buffer} toAsciiTable - 256-byte EBCDIC→ASCII lookup table
 * @param {string} [name] - Human-readable name for the code page
 */
function registerCodepage(cpNumber, toAsciiTable, name = `CP${cpNumber}`) {
  if (toAsciiTable.length !== 256) throw new Error('Code page table must be 256 bytes');
  CODEPAGES[cpNumber] = {
    toAscii: toAsciiTable,
    fromAscii: buildInverse(toAsciiTable),
    name,
  };
}

function getCodepage(cp) {
  const page = CODEPAGES[cp];
  if (!page) {
    // Fall back to CP037 rather than throwing — matches mainframe fallback behavior
    return CODEPAGES[37];
  }
  return page;
}

/**
 * Convert an EBCDIC Buffer to an ASCII string.
 * Non-printable characters outside 0x20–0x7E are replaced with a space.
 *
 * @param {Buffer} buf     - Input buffer of EBCDIC bytes
 * @param {number} [cp=37] - Code page number
 * @returns {string}
 */
function toAscii(buf, cp = 37) {
  const table = getCodepage(cp).toAscii;
  let out = '';
  for (const b of buf) {
    const a = table[b];
    out += String.fromCharCode(a >= 0x20 && a < 0x7F ? a : 0x20);
  }
  return out;
}

/**
 * Convert an ASCII string to an EBCDIC Buffer.
 * Characters with no EBCDIC equivalent are mapped to 0x3F (EBCDIC '?').
 *
 * @param {string} str     - Input ASCII/Latin-1 string
 * @param {number} [cp=37] - Code page number
 * @returns {Buffer}
 */
function fromAscii(str, cp = 37) {
  const table = getCodepage(cp).fromAscii;
  const buf = Buffer.alloc(str.length);
  for (let i = 0; i < str.length; i++) {
    buf[i] = table[str.charCodeAt(i)] ?? 0x3F;
  }
  return buf;
}

/**
 * Pad or truncate an ASCII string to exactly `len` characters, then
 * convert to EBCDIC.  Useful for fixed-length 3270 input fields.
 *
 * @param {string} str     - Input string
 * @param {number} len     - Exact target length in bytes
 * @param {number} [cp=37] - Code page number
 * @returns {Buffer}
 */
function fromAsciiFixed(str, len, cp = 37) {
  return fromAscii(str.padEnd(len, ' ').slice(0, len), cp);
}

/**
 * List all registered code pages.
 * @returns {Array<{number: number, name: string}>}
 */
function listCodepages() {
  return Object.entries(CODEPAGES).map(([num, page]) => ({
    number: parseInt(num, 10),
    name: page.name,
  }));
}

module.exports = { toAscii, fromAscii, fromAsciiFixed, registerCodepage, listCodepages };
